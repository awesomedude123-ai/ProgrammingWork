package com.sri;

//POJO-Plain Old Java Object

public class Student {
	
	int age;
	String name;
	String address;
	int grade;
	double gpa;
	
	public Student(int age, String name, String address, int grade, double gpa){
		super();
		this.age=age;
		this.name=name;
		this.address=address;
		this.grade=grade;
		this.gpa=gpa;
	}//end constructor
	
	
	

}
